package ie.gmit.sw.config;
/**
 * This interface defines the methods required for a class to behave as a parseator.
 * @author RnDMizeD
 *
 */
public interface Parseator { 
	/**
	 * Method to implement in order to be a parseator.
	 * @throws Throwable Exception in case of error.
	 */
	public void parse() throws Throwable;

}
